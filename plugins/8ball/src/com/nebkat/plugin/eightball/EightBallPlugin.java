package com.nebkat.plugin.eightball;

import com.nebkat.junglist.bot.command.Command;
import com.nebkat.junglist.bot.command.CommandEvent;
import com.nebkat.junglist.bot.command.CommandFilter;
import com.nebkat.junglist.bot.command.UserLevel;
import com.nebkat.junglist.bot.plugin.Plugin;
import com.nebkat.junglist.irc.Irc;
import com.nebkat.junglist.irc.events.EventHandler;

import java.util.Random;

public class EightBallPlugin extends Plugin {
    private static final String[] ANSWERS = new String[] {
            "It is certain",
            "It is decidedly so",
            "Without a doubt",
            "Yes - definitely",
            "You may rely on it",
            "As I see it, yes",
            "Most likely",
            "Outlook good",
            "Yes",
            "Signs point to yes",
            "Reply hazy, try again",
            "Ask again later",
            "Better not tell you now",
            "Cannot predict now",
            "Concentrate and ask again",
            "Don't count on it",
            "My reply is no",
            "My sources say no",
            "Outlook not so good",
            "Very doubtful"
    };

    private Random mRandom = new Random();

    @EventHandler
    @CommandFilter("8ball")
    public void on8BallCommand(CommandEvent e) {
        Irc.message(e.getSession(), e.getTarget(), e.getSource().getNick() + ": " + ANSWERS[mRandom.nextInt(ANSWERS.length)]);
    }

    @Override
    public void onCreate() {
        getBot().getCommandManager().registerCommand(new Command("8ball", this, "Magic 8-Ball", null, UserLevel.NORMAL, false, "eightball"));
    }
}